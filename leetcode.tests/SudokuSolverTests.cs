    [Fact]
    public void TestSudokuUtils()
    {
        char[][] board =
        [
            ['5', '3', '.', '.', '7', '.', '.', '.', '.'], ['6', '.', '.', '1', '9', '5', '.', '.', '.'],
            ['.', '9', '8', '.', '.', '.', '.', '6', '.'], ['8', '.', '.', '.', '6', '.', '.', '.', '3'],
            ['4', '.', '.', '8', '.', '3', '.', '.', '1'], ['7', '.', '.', '.', '2', '.', '.', '.', '6'],
            ['.', '6', '.', '.', '.', '.', '2', '8', '.'], ['.', '.', '.', '4', '1', '9', '.', '.', '5'],
            ['.', '.', '.', '.', '8', '.', '.', '7', '9']
        ];
        var quad1 = board.GetQuadrantValues(0, 0);
        var quad2 = board.GetQuadrantValues(1, 1);
        var quad3 = board.GetQuadrantValues(2, 2);
        var quad4 = board.GetQuadrantValues(1, 2);
        Assert.Equal(quad1, quad2);
        Assert.Equal(quad2, quad3);
        Assert.Equal(quad3, quad4);
        
        quad1 = board.GetQuadrantValues(3, 0);
        quad2 = board.GetQuadrantValues(4, 1);
        quad3 = board.GetQuadrantValues(5, 2);
        quad4 = board.GetQuadrantValues(4, 2);
        Assert.Equal(quad1, quad2);
        Assert.Equal(quad2, quad3);
        Assert.Equal(quad3, quad4);

        quad1 = board.GetQuadrantValues(3, 6);
        quad2 = board.GetQuadrantValues(4, 7);
        quad3 = board.GetQuadrantValues(5, 7);
        quad4 = board.GetQuadrantValues(4, 8);
        Assert.Equal(quad1, quad2);
        Assert.Equal(quad2, quad3);
        Assert.Equal(quad3, quad4);

        quad1 = board.GetQuadrantValues(0, 0);
        Assert.Equal(['5', '3', '6', '9', '8'], quad1);
        
        quad1 = board.GetQuadrantValues(4, 4);
        Assert.Equal(['6', '8', '3', '2'], quad1);

        var column0 = board.GetColumnValues(3, 0);
        Assert.Equal(['5', '6', '8', '4', '7'], column0);

        var column = board.GetColumnValues(3, 1);
        Assert.Equal(['3', '9', '6'], column);

        var row = board.GetRowValues(3, 0);
        Assert.Equal(['8', '6', '3'], row);
    }

